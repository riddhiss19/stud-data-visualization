    public boolean LoginValidation()
    {
        String Email=mail.getText();
        String Password = password.getText();
        
        if(Email.equals(""))
        {
            JOptionPane.showMessageDialog(this,"Enter Email");
            return false;
        }
        if(Password.equals(""))
        {
            JOptionPane.showMessageDialog(this,"Enter Password");
            return false;
        }
        return true;
        
    }
    
    public int login()
    {
        email_g=mail.getText();
        String Password = password.getText();
        String Tabel=tabel.getText();
        
        try
        {
            
           con = DriverManager.getConnection("jdbc:mysql://localhost:3306/OOP_CP","root","");
           PreparedStatement pst =con.prepareStatement("Select * from login where Email =? and Password =?");
           
           pst.setString(1, email_g);
           pst.setString(2, Password);
           
           
           ResultSet rs = pst.executeQuery();
           
           if(rs.next())
           {
               JOptionPane.showMessageDialog(this,"Login Sucessfull");
               InsertData_OOP qwe=new InsertData_OOP(Tabel);
               
               qwe.setVisible(true);
               this.dispose();
           }
           else
           {
               JOptionPane.showMessageDialog(this,"Login Unsucessfull");
           }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        return 0;
        
    }
    
    
